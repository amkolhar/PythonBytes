# Definition of Function
def add(a:int, b:int):

    total = a+ b

    return total


def subtract(a:int, b:int):

    diff = a - b

    return diff

# Use of function
c = 10
d = 8

total = add(c, d)
diff = subtract(c, d)

print(total)
print(diff)

e = 30
f = 20

ef_total = add(e, f)
ef_diff = subtract(e, f)

print(ef_total, ef_diff)

